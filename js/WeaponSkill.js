function WeaponSkill(sword, lance, axe, bow, anima, dark, light, staff) {
    // Represent the weapon skill of a unit.
    // Weapon ranks from 0-6: no skill, E, D, C, B, A, S.
    this.sword = sword;
    this.lance = lance;
    this.axe = axe;
    this.bow = bow;
    this.anima = anima;
    this.dark = dark;
    this.light = light;
    this.staff = staff;
}

